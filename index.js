let x = 10
let y = 15
let z = 30

let sumOne = Math.pow(x, 3); // The math.pow function works out variable x to the power of 3 (x cubed)
let sumTwo = Math.pow(y, 3);
let sumThree = Math.pow(z, 3);

console.log(sumOne)
console.log(sumTwo)
console.log(sumThree)

console.log(sumOne + sumTwo + sumThree) // This is the sum of all cubed numbers added together